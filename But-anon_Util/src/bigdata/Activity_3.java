    private void countUpStartBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_countUpStartBtnMouseClicked
        // TODO add your handling code here:
        String count = countUp.getText();

        new Thread(new Runnable() {
            int time = Integer.parseInt(count);

            public void run() {
                while (time <= 100) {
                    countUp.setText(String.valueOf(time));
                    time++;
                    try {
                        Thread.sleep(1000L);
                    } catch (InterruptedException ie) {
                        System.out.println(ie.getMessage());
                    }
                }
                while (time >= 100) {
                    countUpPanel.setBackground(Color.RED);
                    countUpPanel.setBackground(Color.PINK);
                }
            }
        }).start();
    }//GEN-LAST:event_countUpStartBtnMouseClicked

    private void countDownStartBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_countDownStartBtnMouseClicked
        // TODO add your handling code here:
        String count = countDown.getText();

        new Thread(new Runnable() {
            int time = Integer.parseInt(count);

            public void run() {
                while (time >= 0) {
                    countDown.setText(String.valueOf(time));
                    time--;
                    try {
                        Thread.sleep(1000L);
                    } catch (InterruptedException ie) {
                        System.out.println(ie.getMessage());
                    }
                }
                while (time <= 0) {
                    countDownPanel.setBackground(Color.CYAN);
//                    countDownPanel.setBackground(Color.blue);
                }
            }
        }).start();
    }//GEN-LAST:event_countDownStartBtnMouseClicked

    public void currentTime() {
        new Thread(new Runnable() {
            public void run() {
                try {
                    while (true) {
                        Date d = new java.util.Date();
                        SimpleDateFormat sdf = new SimpleDateFormat("hh:MM:ss a");
                        currentTime.setText(sdf.format(d));
                        Thread.sleep(1000);
                        Activity_3.class.cast(currentTime);
                    }

                } catch (Exception e) {
                }
            }
        }).start();
    }

