    boolean stop = false;

    private void startBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_startBtnMouseClicked
        // TODO add your handling code here:
        stop = false;
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int o = 0;; o++) { // this is an infinite loop
                    for (int h = 0;; h++) {//
                        for (int i = 0; i < 60; i++) { // this for minutes
                            for (int j = 0; j < 60; j++) { // j is for the seconds
                                for (int k = 0; k < 100; k++) { // k is the centiseconds
                                    if (stop) {
                                        break;
                                    }
                                    NumberFormat nf = new DecimalFormat("00");
                                    hours.setText(nf.format(h));
                                    minutes.setText(nf.format(i));
                                    seconds.setText(nf.format(j));
                                    centiseconds.setText(nf.format(k));
                                    try {
                                        Thread.sleep(10);
                                    } catch (Exception e) {
                                    }

                                }
                                if (stop) { // if stop button is clicked then the milliseconds will stop
                                    break;
                                }
                            }
                            if (stop) { // if stop button is clicked then the seconds will stop
                                break;
                            }
                        }
                        if (stop) { // if stop button is clicked then the minutes will stop
                            break;
                        }
                    }
                    
                }
            }
        }
        ).start();    
    }//GEN-LAST:event_startBtnMouseClicked

